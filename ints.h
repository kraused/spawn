
#ifndef SPAWN_INTS_H_INCLUDED
#define SPAWN_INTS_H_INCLUDED 1

#include <stdint.h>

/* shorter names for the standard types and without this
 * annoying _t appened. We spell out the signedness of the
 * type.
 */
typedef int8_t  	si8;
typedef uint8_t 	ui8;
typedef int16_t 	si16;
typedef uint16_t	ui16;
typedef int32_t 	si32;
typedef uint32_t	ui32;
typedef int64_t 	si64;
typedef uint64_t	ui64;

typedef long long         	ll;
typedef unsigned long long	ull;

#endif

